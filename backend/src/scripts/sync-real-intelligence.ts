async function main() {
  const client = new Client(RAILWAY_DB);
  await client.connect();
  console.log('✅ 已连接 Railway 数据库\n');

  // 清空旧的情报数据
  await client.query('DELETE FROM "intelligence"');
  console.log('🗑️ 已清空旧情报数据\n');

  // 插入新的真实数据
  let count = 0;
  for (const item of realIntelligence) {
    try {
      await client.query(
        `INSERT INTO "intelligence" (
          title, summary, content, source, "sourceUrl", category,
          tags, "publishTime", "createdAt"
        ) VALUES ($1, $2, $3, $4, $5, $6, $7, $8, NOW())`,
        [
          item.title,
          item.summary,
          item.content,
          item.source,
          item.sourceUrl,
          item.category,
          item.tags,
          item.publishedAt
        ]
      );
      count++;
      console.log(`  ✅ ${count}. ${item.title.substring(0, 40)}...`);
    } catch (err) {
      console.error(`  ❌ 失败: ${item.title}`, err.message);
    }
  }

  console.log(`\n✅ 成功导入 ${count} 条真实资讯！`);
  await client.end();
}

main().catch(console.error);
