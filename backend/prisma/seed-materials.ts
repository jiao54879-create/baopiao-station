async function main() {
  console.log('开始种子数据导入...');
  
  // 先清空
  await prisma.material.deleteMany();
  console.log('已清空现有素材数据');

  let count = 0;
  for (const m of materials) {
    await prisma.material.create({
      data: {
        category: m.category,
        title: m.title,
        content: m.content,
        tags: JSON.stringify(m.tags),
        source: m.source,
      }
    });
    count++;
    if (count % 10 === 0) {
      console.log(`已导入 ${count}/${materials.length} 条`);
    }
  }
  
  console.log(`✅ 成功导入 ${materials.length} 条保险素材数据`);
}

